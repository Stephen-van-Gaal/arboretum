  local out; out="$(bash "$HC" "$d" 2>&1)"
  if echo "$out" | grep -q "Unowned:.*loose.php"; then
    fail_case "// owner: after <?php opener not detected"
  else
    pass "PHP <?php opener skipped; // owner: marker detected"
  fi
  rm -rf "$d"
}

test_php_cli_shebang_opener_marker_owns() {
