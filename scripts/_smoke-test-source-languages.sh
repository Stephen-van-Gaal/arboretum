# --- Task 4: unknown-prefix diagnostic (advisory, config-driven) ---

test_unknown_prefix_diagnostic() {
  local d; d="$(make_fixture)"
  printf 'source_languages:\n  - py\n  - xyz\n' > "$d/.arboretum.yml"
  (cd "$d" && git add -A && git commit -qm init)
  local out rc
  out="$(bash "$HC" "$d" 2>&1)"; rc=$?
  if echo "$out" | grep -qi "xyz.*no comment prefix"; then
    pass "unknown enforced language emits a prefix diagnostic"
  else
    fail_case "no diagnostic for enforced language 'xyz' lacking a prefix"
  fi
  # config-only: the diagnostic is advisory, so it must NOT block (exit != 1)
  if [ "$rc" = "1" ]; then
    fail_case "unknown-prefix diagnostic incorrectly blocking (exit 1)"
  else
    pass "unknown-prefix diagnostic is advisory (exit $rc)"
  fi
  rm -rf "$d"
}

# --- Task 5: Half C undeclared-source-type discovery (advisory) ---

test_halfc_warns_undeclared_sql() {
  local d; d="$(make_fixture)"   # no .arboretum.yml → default [py]
  echo "SELECT 1;" > "$d/src/a.sql"
  echo "SELECT 2;" > "$d/src/b.sql"
  (cd "$d" && git add -A && git commit -qm init)
  local out; out="$(bash "$HC" "$d" 2>&1)"
  local n; n="$(echo "$out" | grep -c "not declared in source_languages")"
  if echo "$out" | grep -q "sql.*not declared in source_languages" && [ "$n" -eq 1 ]; then
    pass "Half C warns once for undeclared .sql with a count"
  else
    fail_case "Half C did not emit exactly one undeclared-.sql nudge (got $n)"
  fi
  rm -rf "$d"
}

test_halfc_silenced_by_ignore() {
  local d; d="$(make_fixture)"
  printf 'source_languages_ignore:\n  - sql\n' > "$d/.arboretum.yml"
  echo "SELECT 1;" > "$d/src/a.sql"
  (cd "$d" && git add -A && git commit -qm init)
  if bash "$HC" "$d" 2>&1 | grep -q "sql.*not declared"; then
    fail_case "ignore-list did not silence Half C"
  else
    pass "source_languages_ignore silences Half C"
  fi
  rm -rf "$d"
}

test_halfc_quiet_for_py_sh_only() {
  local d; d="$(make_fixture)"
  add_spec_owning "$d" alpha "src/alpha.py"
  echo "# owner: alpha" > "$d/src/alpha.py"
  printf '#!/usr/bin/env bash\necho hi\n' > "$d/src/helper.sh"   # sh excluded from discovery
  (cd "$d" && git add -A && git commit -qm init)
  if bash "$HC" "$d" 2>&1 | grep -q "not declared in source_languages"; then
    fail_case "Half C nagged a .py+.sh-only repo (backward-compat break)"
  else
    pass "Half C silent for a .py+.sh-only repo"
  fi
  rm -rf "$d"
}

test_default_does_not_scan_ts
test_optin_flags_unowned_ts
test_marker_owns_enforced_sql
test_generated_banner_marker_owns
test_crlf_marker_owns
test_php_opener_marker_owns
test_php_cli_shebang_opener_marker_owns
test_bad_marker_still_unowned
test_unknown_prefix_diagnostic
test_halfc_warns_undeclared_sql
test_halfc_silenced_by_ignore
test_halfc_quiet_for_py_sh_only

[ "$FAILED" -eq 0 ] && echo "ALL PASS" || exit 1
